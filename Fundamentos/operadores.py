#operadores numericos
a = 10
b = 3
print("Suma: ", a + b)
print("Resta: ", a - b)
print("Multiplicacion: ", a * b)
print("Division: ", a / b)

#Modulo es el restante de la division
print("Modulo: ", a % b)

#Doble division
print("Te da la parte entera de la division : ", a // b)

#potenciacion
print("potenciacion: ", a ** b)#a es el numero a potenciar y b las veces que se va potenciar

#shortcuts
a += 2
a -= 2
a *= 2
a /= 2
print(a)

